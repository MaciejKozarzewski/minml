namespace SIMD_NAMESPACE
{
#if COMPILED_WITH_AVX
	static inline __m256d get_cutoff_mask_pd(int num) noexcept
	{
		assert(0 <= num && num <= 4);
		return _mm256_castsi256_pd(_mm256_load_si256(reinterpret_cast<const __m256i*>(internal::int64_masks[num])));
	}
	static inline __m256 get_cutoff_mask_ps(int num) noexcept
	{
		assert(0 <= num && num <= 8);
		return _mm256_castsi256_ps(_mm256_load_si256(reinterpret_cast<const __m256i*>(internal::int32_masks[num])));
	}
	static inline __m256i get_cutoff_mask_i(int num) noexcept
	{
		assert(0 <= num && num <= 32);
		return _mm256_load_si256(reinterpret_cast<const __m256i*>(internal::int8_masks[num]));
	}
#elif COMPILED_WITH_SSE2
	static inline __m128d get_cutoff_mask_pd(int num) noexcept
	{
		assert(0 <= num && num <= 2);
		return _mm_castsi128_pd(_mm_load_si128(reinterpret_cast<const __m128i*>(internal::int64_masks[num])));
	}
	static inline __m128 get_cutoff_mask_ps(int num) noexcept
	{
		assert(0 <= num && num <= 4);
		return _mm_castsi128_ps(_mm_load_si128(reinterpret_cast<const __m128i*>(internal::int32_masks[num])));
	}
	static inline __m128i get_cutoff_mask_i(int num) noexcept
	{
		assert(0 <= num && num <= 16);
		return _mm_load_si128(reinterpret_cast<const __m128i*>(internal::int8_masks[num]));
	}
#else
	static inline int get_cutoff_mask(int num) noexcept
	{
		assert(num == 0 || num == 1);
		return (num == 0) ? 0 : -1;
	}
#endif
} /* SIMD_NAMEPACE */

